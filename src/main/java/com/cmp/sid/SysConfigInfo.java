package com.cmp.sid;

//系统配置
public class SysConfigInfo {
	private String sysName;				//系统名称
	private String logo;				//系统LOGO
	private String companyName;			//公司名称
	private String companyShortName;	//公司简称
	private String tel;					//咨询电话	
	private String productConsultion;  //产品咨询 
	private String website;				//网站
	private String copr;				//版权
	
	public String getSysName() {
		return sysName;
	}
	public void setSysName(String sysName) {
		this.sysName = sysName;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyShortName() {
		return companyShortName;
	}
	public void setCompanyShortName(String companyShortName) {
		this.companyShortName = companyShortName;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getProductConsultion() {
		return productConsultion;
	}
	public void setProductConsultion(String productConsultion) {
		this.productConsultion = productConsultion;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public String getCopr() {
		return copr;
	}
	public void setCopr(String copr) {
		this.copr = copr;
	}
	
	
}
